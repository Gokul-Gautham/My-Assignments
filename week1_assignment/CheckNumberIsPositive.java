package week1.week1_assignment;

public class CheckNumberIsPositive {

	public static void main(String[] args) {
		int version = 10;
		if (version>0){
			System.out.println("Number ["+version+"] is Positive");
		}
		else if (version==0){
			System.out.println("Number ["+version+"] is Neutral");
		}
		else {
			System.out.println("Number ["+version+"] is Negative");
		}

	}

}
